package modelo;

public class Chofer {
    private int idChofer;
    private String nombre;
    
    public Chofer(){
        this.idChofer=0;
        this.nombre="";
    }

    public int getIdChofer() {
        return idChofer;
    }

    public void setIdChofer(int idChofer) {
        this.idChofer = idChofer;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
    
}
